enter = int(input("Introdueix un número: "))
flotant = float(input("Introdueix un altre número: "))

print("suma:", enter + flotant)
print("resta:", enter - flotant)
print("multiplicació:", enter * flotant)
print("divisió:", enter / flotant)
print("divisió entera:", enter // flotant)
print("mòdul:", enter % flotant)

